    private void newCustomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newCustomActionPerformed
        if(this.drawing) {
            this.drawing = false;
            playground.setDrawing(false);
            newCustom.setBackground(Color.LIGHT_GRAY);
            newCustom.setOpaque(false);
            playground.timer.start();
        } else {
            this.drawing = true;
            playground.setDrawing(true);
            newCustom.setBackground(Color.DARK_GRAY);
            newCustom.setOpaque(true);
            playground.timer.stop();
        }
    }//GEN-LAST:event_newCustomActionPerformed

