    private void newPresetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newPresetActionPerformed
        playground.setPresetEn(!playground.getPresetEn());
        if (playground.getPresetEn()) {
            newPreset.setBackground(Color.DARK_GRAY);
            newPreset.setOpaque(true);
            playground.setPres(presety.getSelectedIndex());
        } else {
            newPreset.setBackground(Color.LIGHT_GRAY);
            newPreset.setOpaque(true);
        }
                
            
    }//GEN-LAST:event_newPresetActionPerformed

